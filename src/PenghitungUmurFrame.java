    private void jDateChooser1PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jDateChooser1PropertyChange

        stopFetching = true;
        if (peristiwaThread != null && peristiwaThread.isAlive()) {
            peristiwaThread.interrupt(); // Beri sinyal untuk menghentikan thread
        }

        // Kosongkan area peristiwa saat tanggal lahir diubah
        jTextArea1.setText("");

        // Kosongkan field umur dan tanggal ulang tahun berikutnya
        jTextField1.setText("");
        jTextField2.setText("");
    
    }//GEN-LAST:event_jDateChooser1PropertyChange

